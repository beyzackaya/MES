
package mes.model;


public class WarehouseStock {
    private int warehouseStockId;
    private int warehouseStockproductId;
    private int warehouseStockQuantityInStock;
    private int warehouseStockWarehouseId;

    public WarehouseStock() {
    }

    public int getWarehouseStockId() {
        return warehouseStockId;
    }

    public void setWarehouseStockId(int warehouseStockId) {
        this.warehouseStockId = warehouseStockId;
    }

    public int getWarehouseStockproductId() {
        return warehouseStockproductId;
    }

    public void setWarehouseStockproductId(int warehouseStockproductId) {
        this.warehouseStockproductId = warehouseStockproductId;
    }

    public int getWarehouseStockQuantityInStock() {
        return warehouseStockQuantityInStock;
    }

    public void setWarehouseStockQuantityInStock(int warehouseStockQuantityInStock) {
        this.warehouseStockQuantityInStock = warehouseStockQuantityInStock;
    }

    public int getWarehouseStockWarehouseId() {
        return warehouseStockWarehouseId;
    }

    public void setWarehouseStockWarehouseId(int warehouseStockWarehouseId) {
        this.warehouseStockWarehouseId = warehouseStockWarehouseId;
    }
    
    
}
